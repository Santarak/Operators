package operators;

public class arithmeticOperators {

	public static void main(String[] args) {
		int a=9,b=8;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(b--);
		System.out.println(b);
		

	}

}
